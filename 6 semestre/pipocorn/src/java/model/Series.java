package model;


public class Series {
   private int id;
    private String nome;
    private String link;
    private String genero;
    private String foto;
    
     public Series(String nome, String link, String genero,String foto) {

        this.nome = nome;
        this.genero = genero;
        this.link = link;
        this.foto=foto;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    
   

   
    
}
